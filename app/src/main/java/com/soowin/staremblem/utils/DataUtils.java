    public interface MyCallBack {
        void call(long hour, long min);
    }

    /**
     * 获取时间差
     *
     * @param time     "出发时间"
     * @param callBack 回调
     *                 hour min ： 相差 hour 小时  min 秒
     */
    public static void isMoreThanHour(String time, MyCallBack callBack) {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        Date preseTime = null;
        try {
            preseTime = sdf.parse(time);
        } catch (ParseException e) {
            e.printStackTrace();
        }
        Date date = new Date();
        long times = preseTime.getTime() - date.getTime();
        long hour = times / (60 * 60 * 1000);
        long min = times / (60 * 1000) - hour * 60;
        callBack.call(hour, min);
    }


